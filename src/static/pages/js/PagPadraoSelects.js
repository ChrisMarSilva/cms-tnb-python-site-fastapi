async function carregar_lista_padrao( urlPadrao, IdCampo, UrlLista, ExibirBranco = false, ExibirTodos = false, ExibirSelecione = false, ExibirTodasAcoes = false, ExibirTodasFIIs = false, ExibirTodasETFs = false, ExibirTodasBDRs = false, ExibirTodasCriptos = false){
	try {

		 promise = new Promise( (resolve, reject) => {

				if ( IdCampo == 'undefined')  return false;

				$('#'+IdCampo).empty();

				if ( ExibirBranco     == true ) $('#'+IdCampo).append('<option val="" value="" selected></option>');
				if ( ExibirTodos      == true ) $('#'+IdCampo).append('<option val="" value="" selected>Todos</option>');
				if ( ExibirSelecione  == true ) $('#'+IdCampo).append('<option val="" value="" selected>Selecione...</option>');
				if ( ExibirTodasAcoes == true ) $('#'+IdCampo).append('<option val="ACAO" value="ACAO">Todas Ações</option>');
				if ( ExibirTodasFIIs  == true ) $('#'+IdCampo).append('<option val="FII" value="FII">Todos FIIs</option>');
				if ( ExibirTodasETFs  == true ) $('#'+IdCampo).append('<option val="ETF" value="ETF">Todos ETFs</option>');
				if ( ExibirTodasBDRs  == true ) $('#'+IdCampo).append('<option val="BDR" value="BDR">Todas BDRs</option>');
				if ( ExibirTodasCriptos  == true ) $('#'+IdCampo).append('<option val="BDR" value="CRIPTO">Todas Criptos</option>');

				$.ajax({
					cache   : "false",
					dataType: "json",
					async   : true,
					type    : "POST",
					url     : urlPadrao + "listas/" + UrlLista,
					// data    : {  },
					success: function(result) {

						var resultado = result.data.Resultado;
						var mensagem  = result.data.Mensagem;
						var lista     = result.data.Lista;

						if (resultado == "NSESSAO") {
							$(location).attr('href', urlPadrao + '/login');
							return false;
						} else if (resultado == "NOK") {
							CriarAlertaPrinc(TP_ALERTA_AVISO, mensagem);
							return false;
						} else if (resultado == "OK") {
						    try {
                                $.each(lista, function (index, value) { $('#'+IdCampo).append('<option val="'+ value[0] +'" value="'+ value[0] +'">'+value[1]+'</option>'); });
                                fDefinirPadraoSelect(IdCampo);
                                return true;
                            } catch(e) {
                              //  fCriarAlertaPrinc(TP_ALERTA_ERRO, MSG_ALERTA_ERRO);
                            }
						} else {
							fCriarAlertaPrinc(TP_ALERTA_ERRO, mensagem);
							return false;
						}

					},
					error: function (request, status, error) { // error: function(data) {  // error: function (request, status, error) {
						fCriarAlertaPrinc(TP_ALERTA_ERRO, MSG_ALERTA_ERRO); // request.responseText
						return false;
					}
				});

			resolve(true);
			// reject("aaaaaaaaaaaa") // reject(new Error("aaaaaaaaaaaa!"));
		})
		.then( txt => {
			//console.log('Sucesso: ' + txt);
		})
		.catch( txt => {
			fCriarAlerta("AreaAlertaPrinc",TP_ALERTA_ERRO, MSG_ALERTA_ERRO);
		});

	} catch(e) {
		fCriarAlertaPrinc(TP_ALERTA_ERRO, MSG_ALERTA_ERRO);
	}
}

